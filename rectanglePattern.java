package loopstmt;

public class rectanglePattern {
	public static void main(String[] args) 
	{
		//printing rectangle of stars (3 rows x 4 cols )
		for(int i = 0; i < 3; i ++)
		{
			for(int j = 0; j< 4 ;j ++)
			{
				System.out.print("*" + " ");
			}
			System.out.println();
		}
		
	}
}
