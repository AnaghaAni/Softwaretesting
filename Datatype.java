package myprj;

public class Datatype {
public static void main(String args[])
{
	byte a =-128;
	int i =128;
	float f=128.2f;
	double d=78.19;
	char c='c';
	boolean b= true;
	
	System.out.println(a);
	System.out.println(i);
	System.out.println(f);
	System.out.println(d);
	System.out.println(c);
	System.out.println(b);
	
}
}
